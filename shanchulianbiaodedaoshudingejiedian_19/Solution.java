// Definition for singly-linked list.
class ListNode {
    int val;
    ListNode next;

    ListNode(int x) {
        val = x;
    }
}

public class Solution {
    public ListNode removeNthFromEnd(ListNode head, int n) {
        if (head == null) {
            return null;
        }
        ListNode dummy = new ListNode(-1);
        dummy.next = head;
        ListNode curr = dummy;
        for (int i = 0; i < n && curr != null; i++) {
            curr = curr.next;
        }
        if (curr == null) {
            return head;
        }
        ListNode pre = dummy;
        while (curr.next != null) {
            pre = pre.next;
            curr = curr.next;
        }
        pre.next = pre.next.next;
        return dummy.next;
    }

}
